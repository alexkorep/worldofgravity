	static boolean getLevel(final int level, int[][] o_map) {
		if (level >= m_levels.length) {
			return false;
		}
		
		parseMap(m_levels[level], o_map);
		return true;
	}

	private static void parseMap(String text, int[][] o_map) {
		int pos = 0;
		for (int row = 0; row < o_map.length; ++row) {
			int[] aRow = o_map[row];
			
			for (int col = 0; col < aRow.length; ++col) {
				if (pos < text.length()) {
					final char c = text.charAt(pos);
					aRow[col] = c - '0';
					pos++;
				}
			}
		}
	}
